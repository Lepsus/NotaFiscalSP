<?php
namespace NotaFiscalSP\Responses;

class BasicResponse extends BaseResponse{
    public $response;

    /**
     * @return mixed
     */
    public function getResponse()
    {
        return $this->response;
    }

    /**
     * @param mixed $response
     */
    public function setResponse($response)
    {
        $this->response = $response;
    }

}